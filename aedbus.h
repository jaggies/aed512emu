#ifndef AEDBUS_H
#define AEDBUS_H

#include <vector>
#include <cinttypes>
#include "bus.h"
#include "mapper.h"
#include "ram.h"
#include "rom.h"
#include "68B21.h"
#include "68B50.h"

class AedBus : public BUS {
    public:
        AedBus();
        virtual ~AedBus();
        uint8_t read(int addr) override { return _mapper.read(addr); }
        void write(int addr, unsigned char value) override { _mapper.write(addr, value); }
        void doVideoIrq() {
            _pia1->assertLine(M68B21::CB1);
        }
    private:
        Mapper _mapper;
        std::vector<uint8_t> _videoMemory;
        M68B21 * _pia0;
        M68B21 * _pia1;
        M68B21 * _pia2;
        M68B50 * _sio0;
        M68B50 * _sio1;
};

#endif // AEDBUS_H
